                        متد Reflect.getPrototypeOf
                        این متد همانند Object.getPrototypeOf مقدار prototype شئ مورد نظرتون رو برگشت میده و میتونین از
                        اون
                        استفاده کنید. این متد فقط یک ورودی رو دریافت میکنه و اون هم شئ مورد نظر برای دریافت prototype می
                        باشد.
                        اگر شئ مورد نظر ویژگی های به ارث برده شده نداشته باشد، مقدار null برگشت داده میشه. همچنین اگر
                        ورودی
                        مورد
                        نظر شئ نباشد یک ارور در Console نمایش داده خواهد شد.

